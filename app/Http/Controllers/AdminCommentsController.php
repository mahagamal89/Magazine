     */
    public function update(Request $request, $id)
    {
        $comment = Comment::findOrFail($id);
        //Check its status and reverse it
        $comment->is_active == 0 ? $comment->is_active = 1 : $comment->is_active = 0;
        $comment->save();
        return back();
    }

